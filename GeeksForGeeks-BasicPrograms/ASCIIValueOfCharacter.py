def getASCIIValue(char):
    return ord(char)


if __name__ == "__main__":
    print(f"ASCI Value of {'A'} is - {getASCIIValue('A')}")
    print(f"ASCI Value of {'Z'} is - {getASCIIValue('Z')}")
    print(f"ASCI Value of {'a'} is - {getASCIIValue('a')}")
    print(f"ASCI Value of {'z'} is - {getASCIIValue('z')}")
    print(f"ASCI Value of {'@'} is - {getASCIIValue('@')}")
    print(f"ASCI Value of {'#'} is - {getASCIIValue('#')}")